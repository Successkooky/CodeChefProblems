import java.util.*;
public class CodeChef34 {
    public static void main(String args[]){
        Scanner input=new Scanner(System.in);
        int T=input.nextInt();
        for (int i = 0; i < T; i++) {
            int X=input.nextInt();
            int next=7-X;
            System.out.println(next);
        }
    }
    
}
